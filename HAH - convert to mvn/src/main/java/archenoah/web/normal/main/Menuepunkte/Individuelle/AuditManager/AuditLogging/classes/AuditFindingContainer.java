    private Boolean dbReadTopic(){
        
        DBClass db = new DBClass();
        
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_All();
        /******************************** Table ****************************************/
        db.DB_Data_Get.DB_Tabellen.DB_set_Tabelle_Einzeln(tbl);
        /******************************************************************************/
        
        db.DB_Data_Get.DB_Filter.DB_WHERE_Allgemein(tbl, "ALF_ID", "=", topId.toString(), "");
        
//        db.debugNextQuery(true);
        
        Container con = db.DB_Data_Get.DB_SEND_AND_GET_Container();
        
        if(con.size() > 0){
            
            Object itemId = con.getItemIds().iterator().next();
            
            Integer logId = (Integer) con.getContainerProperty(itemId, "ALF_ID_AUDIT").getValue();
            String topTitle = (String) con.getContainerProperty(itemId, "ALF_TITLE").getValue();
            String topResult = (String) con.getContainerProperty(itemId, "ALF_OBSERVATIONS").getValue();
            String topHelp = (String) con.getContainerProperty(itemId, "ALF_HELP").getValue();
            Integer rating = (Integer) con.getContainerProperty(itemId, "ALF_RATING").getValue();
            
            
            setFields(logId, topTitle, topResult, rating, topHelp);
            
            return true;
        }else{
            return false;
        }
        
        
        
    }
    
    private Integer dbCreateTopic() {
        Integer res = 0;
        
        
        DBClass db = new DBClass();
        /******************************** Table ****************************************/
        db.DB_Data_Insert.DB_Tabellen.DB_set_Tabelle_Einzeln(tbl);
        /******************************************************************************/
        
        db.DB_Data_Insert.DB_Daten.DB_Data(tbl, "ALF_ID_AUDIT", logId.toString());
        db.DB_Data_Insert.DB_Daten.DB_Data(tbl, "ALF_TITLE",  topTitle);
        
//        db.debugNextQuery(true);
        
        res = db.DB_Data_Insert.DB_Insert();
        
        if(onSave != null) {
            onSave.saveAction();
        }
        
        return res;
    }
    
    private void dbSaveTopic(){
        
        if(topId == null){
            System.out.println("topId is null!");
            return;
        }
        
        DBClass db = new DBClass();
        /******************************** Table ****************************************/
        db.DB_Data_Update.DB_Tabellen.DB_set_Tabelle_Einzeln(tbl);
        /******************************************************************************/
        
        db.DB_Data_Update.DB_Daten.DB_Data(tbl, "ALF_ID_AUDIT", logId.toString());
        db.DB_Data_Update.DB_Daten.DB_Data(tbl, "ALF_TITLE",  topTitle);
        db.DB_Data_Update.DB_Daten.DB_Data(tbl, "ALF_OBSERVATIONS",  fieldDescription.getValue());
        db.DB_Data_Update.DB_Daten.DB_Data(tbl, "ALF_HELP",  fieldHelp.getValue());
        db.DB_Data_Update.DB_Daten.DB_Data(tbl, "ALF_RATING",  fieldRating.getValue() != null ? fieldRating.getValue().toString() : null);
        
        db.DB_Data_Update.DB_Filter.DB_WHERE_Allgemein(tbl, "ALF_ID", "=", topId.toString(), "");
        
//        db.debugNextQuery(true);
        
        if(onSave != null) {
            onSave.saveAction();
        }
        
        db.DB_Data_Update.DB_Update();

    }
    
    private Long dbDeleteTopic(){
        
        if(topId == null){
            System.out.println("topId is null!");
            return null;
        }
        
        DBClass db = new DBClass();
        db.DB_Data_Delete.DB_Tabellen.DB_set_Tabelle_Einzeln(tbl);
        
        db.DB_Data_Delete.DB_Filter.DB_WHERE_Allgemein(tbl, "ALF_ID", "=", topId.toString(), "");
        
//        db.debugNextQuery(true);
        
        if(onSave != null) {
            onSave.saveAction();
        }
        
        return db.DB_Data_Delete.DB_Delete();

    }
    
    
