    private void deleteData(final String id){
        
        I18nManager.global(I18nGlobalMessages.confirm_delete, new MessageBoxListener() {
            
            @Override
            public void buttonClicked(ButtonId arg0) {
                if(ButtonId.YES == arg0) {
                    dbDeleteData(id);
                }
            }
        });
    }
    
    private void dbGetContainer() {
        CMS_Config_Std std = CMS_Config_Std.getInstance();

        DBClass db = new DBClass();
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("SAD_ID");
        
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("CONCAT(IFNULL(CONCAT(ASA_NAME, ' '),''),SAD_NAME,' ',SAD_VORNAME)", "name");
        
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("SAD_FIRMA");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("SAD_TEL");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("SAD_GEBIET");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("PSLV_NAME");

        db.DB_Data_Get.DB_Tabellen.DB_set_Tabelle_Verknüpfung("cust_stammdaten_aussendienst", "LEFT JOIN", "cms_auth_stammdaten_anrede",
                "SAD_ID_ANREDE", "ASA_ID");
        db.DB_Data_Get.DB_Tabellen.DB_set_Tabelle_Verknüpfung("cust_stammdaten_aussendienst", "RIGHT JOIN", "cust_projekte_stammdaten_liste",
                "SAD_ID_PROJEKT", "PSLV_ID");
        
        Container container = db.DB_Data_Get.DB_SEND_AND_GET_Container();

        if (userTableUpdater != null) {
            userTableUpdater.updateTable(container);
        } else {
            tbl_data.setContainerDataSource(container);
        }

    }
    
    private void dbDeleteData(String id){
    
        CMS_Config_Std std = CMS_Config_Std.getInstance();
    
        DBClass db = new DBClass();
        db.DB_Data_Delete.DB_Tabellen.DB_set_Tabelle_Einzeln("cust_stammdaten_aussendienst");
        db.DB_Data_Delete.DB_Filter.DB_WHERE_Allgemein("cust_stammdaten_aussendienst", idcol, "=", id , "");
        db.DB_Data_Delete.DB_Delete();
    
        I18nManager.global(I18nGlobalNotifiers.entry_deleted);
    }
    
