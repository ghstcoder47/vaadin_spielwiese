    /******************** Database *****************/
    private void fillDbMap() {
        
        String table = dataTableName + ":";
        
        dbMap = new HashMap<>();
        dbMap.put(patient, table + "OMS_ID_PATIENT");
        dbMap.put(product, table + "OMS_ID_PRODUCT");
        dbMap.put(quantity, table + "OMS_QUANTITY");
        dbMap.put(country, table + "OMS_KEY_COUNTRY");

    }

    private HashMap<Object, String> prepareDbMap(){
        HashMap tempMap = new HashMap<Object, String>();
        tempMap.putAll(dbMap);
        
        String table = dataTableName + ":";
        
        tempMap.put(rrule.getField(PART.START), table + "OMS_RRULE_START");
        tempMap.put(rrule.getField(PART.RRULE), table + "OMS_RRULE");
        tempMap.put(rrule.getField(PART.CUSTOM), table + "OMS_RRULE_CUSTOM");
        
        return tempMap;
    }
    
    private void dbFillForm() {
        DBClass db = new DBClass();
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_All();
        db.DB_Data_Get.DB_Tabellen.DB_set_Tabelle_Einzeln(dataTableName);
        db.DB_Data_Get.DB_Filter.DB_WHERE_Allgemein(dataTableName, dataIdCol, "=", dataSetId, "");

        Container con = db.DB_Data_Get.DB_SEND_AND_GET_Container();
        Item item = MyUtils.getFirstItemFromContainer(con);
       
        if(item != null) {
            
            fb = new Form_Bind();
            fb.Get_and_Fill(con, dbMap);
            
            RruleItem ritem = new RruleItem(item);
            ritem.setPropertyFor(PART.START, "OMS_RRULE_START");
            ritem.setPropertyFor(PART.RRULE, "OMS_RRULE");
            ritem.setPropertyFor(PART.CUSTOM, "OMS_RRULE_CUSTOM");
            
            rrule.setValue(ritem);
        }
        
    }

    private int dbInsert() {
        DBClass db = new DBClass();
        
        fb = new Form_Bind();
        fb.Insert(db, prepareDbMap());
        
        db.DB_Data_Insert.DB_Tabellen.DB_set_Tabelle_Einzeln(dataTableName);
        
        // dba.DB_Data_Insert.DB_DEBUG_SQL_STRING();
        return db.DB_Data_Insert.DB_Insert();
    }

    private void dbUpdate() {
        DBClass db = new DBClass();
        fb = new Form_Bind();
        fb.Update(db, prepareDbMap());
        
        db.DB_Data_Update.DB_Tabellen.DB_set_Tabelle_Einzeln(dataTableName);
        db.DB_Data_Update.DB_Filter.DB_WHERE_Allgemein(dataTableName, dataIdCol, "=", dataSetId, "");
        
        db.DB_Data_Update.DB_Update();

    }

    private void dbFillPatient() {
        CustomQuery q = new DBClass().CustomQuery;
        q.setSqlString("select PSL_ID, CONCAT(PSA_NAME, ' ', PSL_VORNAME, ' ', PSL_NAME) as patient"
             + "\n " + "from view_patient_stammdaten_liste"
             + "\n " + "left join cust_patient_stammdaten_anrede on PSL_ANREDE = PSA_ID");
//        q.db.debugNextQuery(true);
        
        patient.setContainerDataSource(MyUtils.convertIndex(q.query(), "PSL_ID"));
    }
    
    private void dbFillProduct() {
        CustomQuery q = new DBClass().CustomQuery;
        q.setSqlString("select PP_ID, PP_PRODUCT as product from cust_prescriptions_products");
//        q.db.debugNextQuery(true);

        product.setContainerDataSource(MyUtils.convertIndex(q.query(), "PP_ID"));
    }
    
