    private void dbGetContainer() {
        CMS_Config_Std std = CMS_Config_Std.getInstance();

        DBClass db = new DBClass();

        /************ Set Spalten *************/
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("DISTINCT PSL_ID");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("PSL_ID", "ID");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("PSL_CREATED", "Erstellt");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("CONCAT_WS('', PSL_NAME,', ',PSL_VORNAME, ', ', PSA_NAME,' ',PSL_TITEL)", "Patient");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("PSL_STRASSE", "'Strasse'");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("PSL_ORT", "'Ort'");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("PSL_PLZ", "'PLZ'");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("PSL_COUNTYCODE", "'Land'");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("PSL_TEL", "'Telefon'");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("PSL_HANDY", "'Handy'");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("PSL_EMAIL", "'Email'");

        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("(SELECT group_concat(DISTINCT PSLV_NAME ORDER BY PSLV_NAME ASC SEPARATOR ', ') from cust_verk_haupt INNER JOIN cust_projekte_stammdaten_liste ON cust_verk_haupt.VH_PROJEKT_ID = cust_projekte_stammdaten_liste.PSLV_ID   WHERE VH_PATIENT_ID = PSL_ID)", "'Projekte'");
        db.DB_Data_Get.DB_Spalten.Standard.DB_set_Spalten_Einzeln("(SELECT GROUP_CONCAT(DISTINCT PMF_CODE ORDER BY PMF_DATE_FROM ASC SEPARATOR ', ') FROM cust_patient_monitoring_firms WHERE PMF_ID_PATIENT = PSL_ID)", "'AnyCare'");

        /**************************************/
        /************ Set Tabelle *************/
        db.DB_Data_Get.DB_Tabellen.DB_set_Tabelle_Verknüpfung("view_patient_stammdaten_liste", "LEFT JOIN", "cust_patient_stammdaten_anrede", "PSL_ANREDE", "PSA_ID");
        /**************************************/
        /************ Set Filter *************/
        /**************************************/
        Country uc = MyUtils.getUserData().getCountry();
        if(uc.hasPermissions()) {
            db.DB_Data_Get.DB_Filter.DB_WHERE_In("view_patient_stammdaten_liste", "PSL_COUNTYCODE", uc.getFilterString(), UserData.get().getNurse().isNurse() ? "AND ": "");
        }
        /************ Set Custom *************/
        Filter_Krankenschwester(db);
//        Filter_Arzt(db);
        /**************************************/
        
//        db.debugNextQuery(true);
        Container container = db.DB_Data_Get.DB_SEND_AND_GET_Container();

        if (tableUpdater != null) {
            tableUpdater.updateTable(container);
        } else {
            tbl_data.setContainerDataSource(container);
        }
//        exTb.stop();
    }
    
    private void dbDeleteData(String id){
    
        CMS_Config_Std std = CMS_Config_Std.getInstance();
    
        DBClass db = new DBClass();
        db.DB_Data_Delete.DB_Tabellen.DB_set_Tabelle_Einzeln("cust_patient_stammdaten_liste");
        db.DB_Data_Delete.DB_Filter.DB_WHERE_Allgemein("cust_patient_stammdaten_liste", "PSL_ID", "=", id, "");
        db.DB_Data_Delete.DB_Delete();
    
        I18nManager.global(I18nGlobalNotifiers.entry_deleted);
        
        updateTable(true);

        
    }
    
