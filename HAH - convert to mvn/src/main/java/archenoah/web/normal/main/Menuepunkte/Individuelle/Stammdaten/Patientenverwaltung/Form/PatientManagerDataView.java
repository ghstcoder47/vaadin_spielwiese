    private void Filter_Krankenschwester(DBClass db) {
        if (UserData.get().getNurse().isNurse() == false) {
            return;
        }
        
        CustomQuery q = new DBClass().CustomQuery;
        String sql = "select VH_PATIENT_ID from cust_verk_haupt where VH_NURSE_ID = " + UserData.get().getNurse().getNurseId()
            + "\n " + "UNION DISTINCT"
            + "\n " + "select CVS_PATIENT_ID from cust_verk_subs where CVS_NURSE_ID = " + UserData.get().getNurse().getNurseId();
        q.setSqlString(sql);
        
        Container con = q.query();

        ArrayList<Integer> pids = new ArrayList<Integer>();
        for (Object iid : con.getItemIds()) {
            pids.add(MyUtils.getValueFromItem(con.getItem(iid), "VH_PATIENT_ID", Integer.class));
        }
        
        db.DB_Data_Get.DB_Filter.DB_WHERE_In("view_patient_stammdaten_liste", "PSL_ID",  pids.size() > 0 ? Joiner.on(",").join(pids) : "0", "");

    }


    
