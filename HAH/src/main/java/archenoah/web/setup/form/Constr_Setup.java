	private void musik_ini()
	{
		 // Installer Musik
		 
		 
		
		URL Pfad_URL = getClass ().getProtectionDomain().getCodeSource().getLocation ();
		
		String P_temp =Pfad_URL.toString();
		
		String Pad =P_temp.substring (6, P_temp.length() - 23);
		
		
		String pfad = Pad.replace("%20", " ");
		pfad = pfad +"sound/1.wav";
		
		
		
		File f = new File(pfad );
		sound = null;
		try {
			
			sound = Applet.newAudioClip( f.toURL() );
			
		
		
		} catch (MalformedURLException e) {
			// TODO Automatisch generierter Erfassungsblock
			e.printStackTrace();
		}
		
		 
		 
		
		
	}

