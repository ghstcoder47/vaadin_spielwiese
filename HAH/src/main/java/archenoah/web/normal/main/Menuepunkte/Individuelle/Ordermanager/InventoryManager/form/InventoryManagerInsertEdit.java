    private void componentListeners(){
        
        ValueChangeListener saveEnabledListener = new ValueChangeListener() {
            
            @Override
            public void valueChange(ValueChangeEvent event) {
                
                Boolean enabled = (fieldCountry.getValue() != null && fieldProduct.getValue() != null);
                
                for (Entry<Object, String> entry : disabledMap.entrySet()) {
                    AbstractField field = (AbstractField) entry.getKey();
                    field.setEnabled(enabled);
                }
                btn_save.setEnabled(enabled);
                
            }
        };
        
        fieldProduct.addValueChangeListener(saveEnabledListener);
        fieldCountry.addValueChangeListener(saveEnabledListener);
        
    }
    
