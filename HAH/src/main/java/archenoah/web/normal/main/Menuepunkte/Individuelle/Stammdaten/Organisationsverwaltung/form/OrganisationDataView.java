	private void dbGetContainer() {

	    CustomQuery q = new DBClass().CustomQuery;
        String sql = "SELECT"
		 + "\n " + "	OSL_ID"
		 + "\n " + "	, OSL_NAME"
		 + "\n " + "	, OSL_STRASSE"
		 + "\n " + "	, OSL_PLZ"
		 + "\n " + "	, OSL_ORT"
		 + "\n " + "	, OSL_ZUSATZ"
		 + "\n " + "	, CC_TEXT"
		 + "\n " + "FROM"
		 + "\n " + "	cust_organisation_stammdaten_liste osl"
		 + "\n " + "LEFT JOIN"
		 + "\n " + "	cust_countrycodes cc ON osl.OSL_COUNTRYCODE = cc.CC_CODE AND cc.CC_LNG = 1";


        
        q.setSqlString(sql);
//        q.db.debugNextQuery(true);
        
        try {
        	Container container = q.query();
            
        	if (tableUpdater != null) {
                tableUpdater.updateTable(container);
            } else {
                tbl_data.setContainerDataSource(container);
            }
        	
		} catch (Exception e) {
			System.out.println("exception!");
			UI.getCurrent().getUI().removePollListener(pollListener);
		}
        
    }
    
    private void dbDeleteData(Collection<Integer> ids){
    
        DBClass db = new DBClass();
        db.DB_Data_Delete.DB_Tabellen.DB_set_Tabelle_Einzeln("cust_organisation_stammdaten_liste");
        db.DB_Data_Delete.DB_Filter.DB_WHERE_In("cust_organisation_stammdaten_liste", tableIdCol, Joiner.on(",").join(ids), "");
        db.DB_Data_Delete.DB_Delete();
    
        I18nManager.global(I18nGlobalNotifiers.entry_deleted);
        
        updateTable(true);

    }
    
    private Integer dbCreate() {
        
        if(tbl_data.getValue() == null) {
            return null;
        }
        
        Item item = null;
        
        ArrayList<Integer> generatedKeys = new ArrayList<Integer>();
        
        Integer oslId = MyUtils.getValueFromItem(item, "OSL_ID", Integer.class);
        String oslCc = MyUtils.getValueFromItem(item, "OSL_COUNTRYCODE", String.class);
        String oslName = MyUtils.getValueFromItem(item, "OSL_NAME", String.class);
        String oslStrasse = MyUtils.getValueFromItem(item, "OSL_STRASSE", String.class);
        String oslOrt = MyUtils.getValueFromItem(item, "OSL_ORT", String.class);
        String oslPlz = MyUtils.getValueFromItem(item, "OSL_PLZ", String.class);
        String oslZusatz = MyUtils.getValueFromItem(item, "OSL_ZUSATZ", String.class);
        String oslTel = MyUtils.getValueFromItem(item, "OSL_TEL", String.class);
        String oslFax = MyUtils.getValueFromItem(item, "OSL_FAX", String.class);
        String oslEmail = MyUtils.getValueFromItem(item, "OSL_EMAIL", String.class);
                
        CustomQuery q = new DBClass().CustomQuery;
        String sql = "INSERT INTO `cust_organisation_stammdaten_liste` (OSL_ID, OSL_COUNTRYCODE, OSL_NAME, OSL_STRASSE, OSL_ORT, OSL_PLZ, OSL_ZUSATZ, OSL_TEL, OSL_FAX, OSL_EMAIL) VALUES"
             + "\n " + "("
             + MyUtils.formatSqlValue(oslId) + ", "
             + MyUtils.formatSqlValue(oslCc) + ", "
             + MyUtils.formatSqlValue(oslName) + ", "
    		 + MyUtils.formatSqlValue(oslStrasse) + ", "
			 + MyUtils.formatSqlValue(oslOrt) + ", "
			 + MyUtils.formatSqlValue(oslPlz) + ", "
			 + MyUtils.formatSqlValue(oslZusatz) + ", "
			 + MyUtils.formatSqlValue(oslTel) + ", "
			 + MyUtils.formatSqlValue(oslFax) + ", "
			 + MyUtils.formatSqlValue(oslEmail)
                 + ");";
        q.setSqlString(sql);
//        q.db.debugNextQuery(true);
        
        q.update(false, generatedKeys);
        
        return generatedKeys.iterator().next();
    }
    
